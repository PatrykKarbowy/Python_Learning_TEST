## Installation

Install Pathfinding visualizers

```bash
  pip install pygame
  python Astar.py
  python Djikstra.py
```
    
## Instructions

- LEFT_MOUSE_BUTTON -> Adding start node
- RIGHT_MOUSE_BUTTON -> Adding end node
- MIDDLE_MOUSE_BUTTON -> Adding barriers
- DELETE_KEY with mouse hover -> Removing node

